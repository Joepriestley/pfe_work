            (fêtes patronales) organisées généralement à la même période comme le moussem de Sidi Ouassay ,de Sidi Ouaggag et le moussem de Tazeroualt.</p>
        <h4>PNSM et Développement Durable </h4>
        <p>La région du PNSM compte de nombreux marabouts (tombeau d'un saint) : Sidi toual, Sidi Rbat, Sidi Ouassay , Sidi Boulfdial, Sidi Abderrahman El Khanboubi.
            Ces lieux attirent chaque année des centaines de pèlerins venant de plusieurs régions du MarocDans le cadre de sa mission de contribution au développement local durable, le PNSM œuvre pour
            l'autopromotion de la population locale dans une perspective de durabilité et de respect des impératifs de conservation..</p>
        <p>Le PNSM à ainsi instauré une coopération avec cette population pour développer et valoriser 4 secteurs socio-économiques identifiés d'une manière participative avec
            les groupements, coopératives et associations de douar. Il s'agit de la pêche artisanale, le tourisme durable, l'apiculture et l'artisanat.</p>
        <h4>Pêche Artisanale </h4>
        <p>Dans un contexte d'exploitation intense des ressources halieutiques du littoral du PNSM, il a fallu élaborer une activité de développement communautaire qui vise à
            appuyer la gestion durable et la valorisation des produits de la mer.</p>
        <h4>Ramassage des Moules </h4>
        <p>Le projet de valorisation des moules élaboré avec la population féminine pratiquant le ramassage de cette ressource dans le village de Douira, a pour objectifs de :
        <ul>
            <li>Renforcer la capacité technique et organisationnelle des femmes ramasseuses des moules à travers la création d'une coopérative de production et de commercialisation des produits de la mer ;</li>
            <li>Promouvoir une gestion durable des moules pour réduire la consommation du bois et la pression sur les gisements naturels des moules ;</li>
            <li>Valoriser les produits de la mer dans le parc par la création d'une unité de transformation des produits de la mer gérée par la coopérative féminine.</li>
        </ul>
        </p>
        <h4>Pêché a la Barque </h4>
        <p>La pêche artisanale joue un rôle fondamental dans l'économie des populations locales, raison pour laquelle le PNSM a entamé un processus d'appui
            technique et organisationnel avec les pêcheurs du point de débarquement du douar Douira et Sidi R'bat, à travers les actions suivantes :
        <ul>
            <li>Organisation des pécheurs en coopérative, en vue d'améliorer les conditions d'exercice de ce métier et assurer une meilleure maitrise de la qualité des produits de pêche ;</li>
            <li>Construction d'un local pour la future coopérative des pécheurs, pour la gestion des activités quotidiennes liées à ce secteur ;</li>
            <li>Mise en œuvre d'un programme de formation pour accompagner les pécheurs et renforcer les capacités de gestion des coopératives et maitrise de la qualité.</li>
        </ul>
        </p>
        <h4>Tourisme Durable a l'Oued Massa </h4>
        <p>Composée d'éléments rarement combinées dans la nature : dunes, végétation, eau et rassemblements d'oiseaux,
