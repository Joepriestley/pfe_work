        <h4>Fondements Economiques </h4>
        <p>Les principales ressources économiques de la population vivant dans la zone du PNSM, proviennent de l'agriculture à travers
            la culture d'orge et blé céréalière, de l'élevage extensif et de l'apiculture.
            <br>
            La pêche artisanale à la barque et le ramassage de moules, constituent une composante essentielle dans l'économie de cette population.
        </p>
        <h4>Utilisation de l'Espace </h4>
        <p>Chaque douar se réserve un espace exclusif constitué par l'espace habité, l'espace réservé aux équipements collectifs
            et à l'extension de l'habitat, et enfin l'espace portant les enclos aménagés pour abriter les troupeaux. La zone agricole,
            généralement située à quelque distance de l'habitation, fait aussi partie du territoire du douar mais peut constituer la zone du douar voisin.</p>
        <h4>Manifestation Socioculturelles</h4>
        <p>La région du PNSM compte de nombreux marabouts (tombeau d'un saint) : Sidi toual, Sidi Rbat, Sidi Ouassay , Sidi Boulfdial, Sidi Abderrahman El Khanboubi.
            Ces lieux attirent chaque année des centaines de pèlerins venant de plusieurs régions du Maroc.</p>
